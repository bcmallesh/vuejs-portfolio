package com.evry.portfolio.model;

public class SectionSummary {
private String summary_heading;
private String summary_image;
private String summary_hero_image;

public String getSummary_heading() {
	return summary_heading;
}
public void setSummary_heading(String summary_heading) {
	this.summary_heading = summary_heading;
}
public String getSummary_image() {
	return summary_image;
}
public void setSummary_image(String summary_image) {
	this.summary_image = summary_image;
}
public String getSummary_hero_image() {
	return summary_hero_image;
}
public void setSummary_hero_image(String summary_hero_image) {
	this.summary_hero_image = summary_hero_image;
}


}
